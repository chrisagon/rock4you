export const getGifUrl = (move: DanceMove): string => {
  if (!move.hasGif || !move.cloudflareUrl) {
    return 'https://via.placeholder.com/300x200/1A1A1A/FF6B35?text=GIF+Non+Disponible';
  }
  return move.cloudflareUrl;
};

export const getAllFamilies = (): string[] => {
  const families = danceMoves
    .map(move => move.family)
    .filter(family => family && family.trim() !== '');
  return [...new Set(families)].sort();
};

export const getAllCourses = (): string[] => {
  const courses = danceMoves
    .map(move => move.courseName)
    .filter(course => course && course.trim() !== '');
  return [...new Set(courses)].sort();
};

